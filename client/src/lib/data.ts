export function formatCurrency(amount: number): string {
  return new Intl.NumberFormat('en-AU', { style: 'currency', currency: 'AUD', maximumFractionDigits: 0 }).format(amount);
}

export function formatDate(dateStr: string): string {
  if (!dateStr) return '—';
  try {
    const d = new Date(dateStr);
    if (isNaN(d.getTime())) return dateStr;
    return d.toLocaleDateString('en-AU', { day: '2-digit', month: 'short', year: 'numeric' });
  } catch { return dateStr; }
}

export function calcAge(dob: string): number {
  if (!dob) return 0;
  try {
    const d = new Date(dob);
    if (isNaN(d.getTime())) return 0;
    const today = new Date();
    let age = today.getFullYear() - d.getFullYear();
    const m = today.getMonth() - d.getMonth();
    if (m < 0 || (m === 0 && today.getDate() < d.getDate())) age--;
    return age;
  } catch { return 0; }
}

export function getUnitByNo(unitNo: string): Unit | undefined {
  return units.find(u => u.unitNo === unitNo);
}

export function getDashboardStats() {
  const occupied = units.filter(u => u.status === 'Occupied').length;
  const vacant = units.filter(u => u.status === 'Vacant').length;
  const totalUnits = units.length;
  const occupancyRate = Math.round((occupied / totalUnits) * 100);
  const totalDeferred = units.reduce((sum, u) => sum + (u.totalDeferred || 0), 0);
  const totalSettlement = units.reduce((sum, u) => sum + (u.settlementPrice || 0), 0);
  const totalMarketValue = units.reduce((sum, u) => sum + (u.marketValue || 0), 0);
  const activeLeads = leads.filter(l => l.status === 'Active').length;
  const waitlistLeads = leads.filter(l => l.status === 'Waitlist').length;
  return { occupied, vacant, totalUnits, occupancyRate, totalDeferred, totalSettlement, totalMarketValue, activeLeads, waitlistLeads };
}

